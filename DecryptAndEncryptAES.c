// xtime la macro tim tich cua {02} va modulo {1b}
unsigned char xtime(unsigned char x) {
	return (x & 0x80) ? ((x<<1) ^ 0x1b) : (x<<1);
}
// multiply la ham nhan cac so trong truong GF(2^8)
unsigned char multiply(unsigned char x, unsigned char y) {
	return (((y & 1) * x) ^
       ((y>>1 & 1) * xtime(x)) ^
       ((y>>2 & 1) * xtime(xtime(x))) ^
       ((y>>3 & 1) * xtime(xtime(xtime(x)))) ^
       ((y>>4 & 1) * xtime(xtime(xtime(xtime(x)))))); 
}
// ham mixcolumn tron cac cot cua ma tran state
void mixColumns() {
	int i;
	unsigned char tmp, tm, t;
	
	for(i=0;i<4;i++) {
		t = state[0][i];
		tmp = state[0][i] ^ state[1][i] ^ state[2][i] ^ state[3][i];
		tm = state[0][i] ^ state[1][i];
		tm= xtime(tm);
		state[0][i] ^= tm ^ tmp;
		
		tm = state[1][i] ^ state[2][i];
		tm = xtime(tm);
		state[1][i] ^= tm ^ tmp;
		tm = state[2][i] ^ state[3][i];
		tm = xtime(tm);
		state[2][i] ^= tm ^ tmp;
		tm = state[3][i] ^ t;
		tm = xtime(tm);
		state[3][i] ^= tm ^ tmp;
	}
}

// ham invmixcolumn() la ham nguoc cua mixcolumn trong giai ma
void InvMixColumns() {
  int i;
  unsigned char a, b, c, d;
  for (i = 0; i < 4; ++i)
  { 
    a = state[0][i];
    b = state[1][i];
    c = state[2][i];
    d = state[3][i];

    state[0][i] = multiply(a, 0x0e) ^ multiply(b, 0x0b) ^ multiply(c, 0x0d) ^ multiply(d, 0x09);
    state[1][i] = multiply(a, 0x09) ^ multiply(b, 0x0e) ^ multiply(c, 0x0b) ^ multiply(d, 0x0d);
    state[2][i] = multiply(a, 0x0d) ^ multiply(b, 0x09) ^ multiply(c, 0x0e) ^ multiply(d, 0x0b);
    state[3][i] = multiply(a, 0x0b) ^ multiply(b, 0x0d) ^ multiply(c, 0x09) ^ multiply(d, 0x0e);
  }
}

// ham ma hoa
void cipher() {
	int i,j,round=0;
	// chuyen ban ro thanh mang cac state
	for(i=0;i<4;i++) {
		for(j=0;j<4;j++) {
			state[j][i] = in[i*4+j];
		}
	}
	// cong voi khoa vong dau tien truoc khi vao vong lap
	addRoundKey(0);
	// co Nr vong
	for(round=1;round<Nr;round++) {
		subBytes();
		shiftRows();
		mixColumns();
		addRoundKey(round);
		printf("\n Vong thu %d:\n", round);
		for(i=0;i<16;i++) {
			printf("%02x", state[i]);
		}
	}
	// vong cuoi
	subBytes();
	shiftRows();
	addRoundKey(Nr);
	// luu tru mang trang thai da ma hoa thanh mang out
	for(i=0;i<4;i++) {
		for(j=0;j<4;j++) {
			out[i*4+j] = state[j][i];
		}
	}
}

// invcipher ham giai ma
void invCipher() {
	int i,j,round=0;
	// chuyen ban ma vao mang state
	for(i=0;i<4;i++) {
		for(j=0;j<4;j++) {
			state[j][i] = out[i*4+j];
		}
	}
	// cong voi khoa vong dau tien
	addRoundKey(Nr);
	// co Nr vong
	for(round=Nr-1;round>0;round--) {
		invShiftRows();
		invSubBytes();
		addRoundKey(round);
		InvMixColumns();
	}
	// vong cuoi
	invShiftRows();
	invSubBytes();
	addRoundKey(0);
	// luu tru mang da giai ma ra mang out
	for(i=0;i<4;i++) {
		for(j=0;j<4;j++) {
			out[i*4+j] = state[j][i];
		}
	}
}

