// ham tao khoa vong
void keyExpansion() {
	int i,j;
	unsigned char temp[4], k;
	
	// khoa vong dau tien
	for(i = 0; i < Nk; i++) {
		roundKey[i*4] = key[i*4];
		roundKey[i*4+1] = key[i*4+1];
		roundKey[i*4+2] = key[i*4+2];
		roundKey[i*4+3] = key[i*4+3];
	}
	
	// tat ca cac khoa vong khac duoc tao ra tu cac khoa vong truoc do
	while(i < (Nb * (Nr + 1))) {
		for(j=0; j <4; j++) {
			temp[j] = roundKey[(i-1)*4+j];			
		}
		if(i % Nk ==0) { // 128
			// quay vong 4 byte trong 1 word sang trai
			//[a0,a1,a2,a3] -> [a1,a2,a3,a0]
			
			// ham rotword()
			{
				k = temp[0];
				temp[0] = temp[1];
				temp[1] = temp[2];
				temp[2] = temp[3];
				temp[3] = k;
			}
			
			// subword() la ham lay word 4 byte dau vao va
			// su dung bang sbox voi moi 4 byte tao ra 1 word
			
			// ham subword()
			{
				temp[0] = getSBoxValue(temp[0]);
				temp[1] = getSBoxValue(temp[1]);
				temp[2] = getSBoxValue(temp[2]);
				temp[3] = getSBoxValue(temp[3]);
			}
			temp[0] = temp[0] ^ rcon[i/Nk];
		}else if(Nk > 6 && i % Nk == 4) { // 192
			// ham subword()
			{
				temp[0] = getSBoxValue(temp[0]);
				temp[1] = getSBoxValue(temp[1]);
				temp[2] = getSBoxValue(temp[2]);
				temp[3] = getSBoxValue(temp[3]);
			}
		}
		roundKey[i*4+0] = roundKey[(i-Nk)*4+0] ^ temp[0];
		roundKey[i*4+1] = roundKey[(i-Nk)*4+1] ^ temp[1];
		roundKey[i*4+2] = roundKey[(i-Nk)*4+2] ^ temp[2];
		roundKey[i*4+3] = roundKey[(i-Nk)*4+3] ^ temp[3];
		i++;
	}
}

// ham nay cong khoa vong vao state
// khoa vong cong XOR vao state
void addRoundKey(int round) {
	int i,j;
	for(i=0; i<4;i++) {
		for(j=0;j<4;j++) {
			state[j][i] ^= roundKey[round*Nb*4 + i*Nb+j];
		}
	}
}

// ham subbyte() thuc hien tru cac gia tri trong ma tran state
void subBytes() {
	int i,j;
	for(i=0;i<4;i++) {
		for(j=0;j<4;j++) {
			state[i][j] = getSBoxValue(state[i][j]);
		}
	}
}

// ham modified subbyte thuc hien xao tron cac bit 
void modifiedSubByte(int round) {
	int i,j;
	for(i=0;i<2;i++) {
		unsigned char tmpkey[8];
		for(j=0;j<4;j++) {
			tmpkey[j*2] = roundKey[round];
		}
	}
}

// ham invsubbyte() la ham nguoc subbyte trong giai ma
void invSubBytes() {
	int i,j;
	for(i=0;i<4;i++) {
		for(j=0;j<4;j++) {
			state[i][j] = getSBoxInv(state[i][j]);
		}
	}
}

// ham shiftrows() dich cac hang trong state sang trai
// moi hang dich di bang so vi tri hang do(tru hang dau tien)
void shiftRows() {
	unsigned char temp;
	// quay hang dau tien 1 cot sang trai
	temp = state[1][0];
	state[1][0] = state[1][1];
	state[1][1] = state[1][2];
	state[1][2] = state[1][3];
	state[1][3] = temp;
	
	// quay hang thu hai 2 cot sang trai
	temp = state[2][0];
	state[2][0] = state[2][2];
	state[2][2] = temp;
	
	temp = state[2][1];
	state[2][1] = state[2][3];
	state[2][3] = temp;
	
	// quay hang ba 3 cot sang trai
	temp = state[3][0];
	state[3][0] = state[3][3];
	state[3][3] = state[3][2];
	state[3][2] = state[3][1];
	state[3][1] = temp;
}

